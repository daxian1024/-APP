h3("2.1 数据处理架构设计")
h3("2.1.1 简介")
para("（1）目的与范围：数据处理架构旨在为平台提供稳定、可扩展、可追溯的数据处理能力，覆盖订单、支付、评价、投诉、通知和行为日志等核心数据。架构目标是兼顾实时性和准确性，支撑业务监控、运营分析与智能决策。")
para("（2）背景信息：系统存在多源异构数据，包括关系型事务数据、缓存数据、日志数据与消息流数据。随着订单量和活跃用户增长，平台需要在“秒级监控与日级分析”双场景下稳定运行，要求具备高吞吐、低延迟、容错恢复和扩展能力。")

h3("2.1.2 数据处理技术选择")
para("（1）批处理技术：适用于海量历史数据离线计算，如日报/周报、服务质量分析、用户分群、历史投诉归因等。技术上采用 PySpark 与 pandas/numpy 组合：PySpark 负责大规模分布式处理，pandas/numpy 负责中小规模分析与报表生成。优势是吞吐能力强、成本可控、逻辑可回放。")
para("（2）流处理技术：适用于实时连续计算，如投诉激增告警、订单状态异常检测、热门服务变动监控。技术上采用 Kafka + Flink。Kafka 提供解耦与削峰，Flink 提供窗口计算、状态管理、Checkpoint 容错。优势是低延迟、高吞吐、可追溯。")
para("（3）混合技术：采用离线与实时融合模式。离线用于全量深度分析和模型训练，实时用于告警与在线指标刷新。两条链路共享统一指标口径，通过主题域建模避免“同名不同义”。")

h3("2.1.3 数据处理流程设计")
para("（1）数据接收：数据源包括 MySQL 业务库、应用日志、消息队列。数据格式以 JSON 为主，批量交换可使用 CSV/Parquet。")
para("（2）数据预处理：执行去重、缺失值填补、异常值处理、时间字段标准化、枚举字段映射，确保后续处理的一致性。")
para("（3）核心处理：批处理通过定时任务触发，输出主题指标；流处理通过 Flink 拓扑实时消费 Kafka，按事件时间窗口聚合并输出告警与实时指标。")
para("（4）数据存储：明细结果入 Hive ODS/DWD，服务层指标入 DWS 或 ClickHouse。业务查询仍以 MySQL 为主，实时展示优先读取 ClickHouse 或缓存。")
para("（5）结果输出：输出方式包含 API、数据库写入、CSV 导出。输出格式统一字段命名、时间格式与错误码规范，保障前后端与数据系统的一致性。")

h3("2.1.4 性能与资源优化")
para("（1）并行处理：批处理采用分布式并行，在线接口采用多进程/协程模型与数据库连接池，提高并发能力。")
para("（2）缓存机制：热门服务、权限信息、限流计数、统计快照进入 Redis 缓存，减少数据库热点压力。")
para("（3）资源分配：根据业务峰谷做弹性配置，按模块设置 CPU/内存配额；实时任务与离线任务进行资源隔离。")
para("（4）容错与恢复：关键任务支持重试策略、幂等写入、失败告警、断点续跑；Flink 配置 Checkpoint 与状态恢复。")

h3("2.2 数据分析和挖掘架构设计")
h3("2.2.1 简介")
para("（1）目的与范围：构建面向运营与服务质量改进的数据分析体系，支持订单效率分析、支付转化分析、满意度分析、投诉风险分析和智能推荐。")
para("（2）背景信息：护理服务存在时段、区域、人群、服务类型差异，需要通过数据建模识别规律，辅助运营策略制定与资源调度。")

h3("2.2.2 技术选型")
para("（1）分布式计算技术：采用 Spark/PySpark 进行大规模离线分析，必要时可扩展 Dask。优势是可扩展、容错好、生态成熟。")
para("（2）机器学习技术：基础模型可采用 scikit-learn；深度学习可扩展 TensorFlow/PyTorch；Spark MLlib 用于分布式建模。")
para("（3）数据存储与访问：离线层使用 HDFS/Hive，实时层使用 ClickHouse，检索类场景可引入 Elasticsearch。")

h3("2.2.3 数据预处理")
para("数据预处理包含数据清洗、数据集成、数据转换与特征工程四个阶段。清洗阶段处理脏数据和异常数据；集成阶段统一用户、订单、服务、支付等主题主键；转换阶段完成归一化、分桶、编码；特征工程阶段构建行为频次、时间衰减、服务偏好、投诉概率等特征。")

h3("2.2.4 数据分析和挖掘模型设计")
para("（1）模型选择：针对不同目标使用分类、回归、聚类和关联规则等算法。")
