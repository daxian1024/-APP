h3("3.2.5 通知模块")
para("模块代号：NTF-05。功能：站内通知发送与查询，预留短信/微信渠道字段。触发场景包括订单分配、状态更新、运营通知。管理员可主动发送，普通用户仅查看与自身相关通知。")

